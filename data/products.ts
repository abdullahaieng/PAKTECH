export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.isFeatured);
}

export function getBestSellers(): Product[] {
  return products.filter((p) => p.isBestSeller);
}

export function getNewArrivals(): Product[] {
  return products.filter((p) => p.isNewArrival);
}

export function getFlashSaleProducts(): Product[] {
  return products.filter((p) => p.isFlashSale);
}

export function getRelatedProducts(product: Product, limit = 4): Product[] {
  return products
    .filter((p) => p.category === product.category && p.id !== product.id)
    .slice(0, limit);
}

export function filterProducts(options: {
  search?: string;
  category?: string;
  minPrice?: number;
  maxPrice?: number;
  sortBy?: string;
}): Product[] {
  let filtered = [...products];

  if (options.search) {
    const q = options.search.toLowerCase();
    filtered = filtered.filter(
      (p) =>
        p.name.toLowerCase().includes(q) ||
        p.description.toLowerCase().includes(q) ||
        p.brand.toLowerCase().includes(q)
    );
  }

  if (options.category && options.category !== "all") {
    filtered = filtered.filter((p) => p.category === options.category);
  }

  if (options.minPrice !== undefined) {
    filtered = filtered.filter(
      (p) => (p.salePrice ?? p.price) >= options.minPrice!
    );
  }

  if (options.maxPrice !== undefined) {
    filtered = filtered.filter(
      (p) => (p.salePrice ?? p.price) <= options.maxPrice!
    );
  }

  switch (options.sortBy) {
    case "price-asc":
      filtered.sort((a, b) => (a.salePrice ?? a.price) - (b.salePrice ?? b.price));
      break;
    case "price-desc":
      filtered.sort((a, b) => (b.salePrice ?? b.price) - (a.salePrice ?? a.price));
      break;
    case "rating":
      filtered.sort((a, b) => b.rating - a.rating);
      break;
    case "newest":
      filtered.sort((a, b) => (b.isNewArrival ? 1 : 0) - (a.isNewArrival ? 1 : 0));
      break;
    default:
      filtered.sort((a, b) => (b.isFeatured ? 1 : 0) - (a.isFeatured ? 1 : 0));
  }

  return filtered;
}

export function getPriceRange(): { min: number; max: number } {
  const prices = products.map((p) => p.salePrice ?? p.price);
  return { min: Math.min(...prices), max: Math.max(...prices) };
}
